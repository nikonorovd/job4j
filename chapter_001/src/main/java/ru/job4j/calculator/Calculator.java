
package ru.job4j.calculator;

/**
 *Class calculator
 */

public class Calculator {
    /**
     * Method to add some double
     * @param  first param
     * @param  second param
     * @return result of add
     */

    public double add(double first, double second) {
        return first + second;

    }
    /**
     * Method to subtract some double
     * @param  first param
     * @param  second param
     * @return result of subtract
     */
    public double subtract (double first, double second){
        return first - second;
    }

    /**
     * Method to div some double
     * @param  first param
     * @param  second param
     * @return result of div
     */
    public double div (double first, double second){

        return first / second;
    }

    /**
     * Method to multiple some double
     * @param  first param
     * @param  second param
     * @return result of multiple
     */
    public double multiple (double first, double second)
    {
        return first * second;
    }
    
}